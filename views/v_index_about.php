	        <h2 class="before-summaries">About Calend'Arte</h2>
 
            <hr class="clearme"/>

	        <p>
	        Many arts and music performance organizations and venues have their own websites that include the event 
	        calendar for the season.  Some communities, newspapers and radio and/or TV stations offer a consolidated 
	        arts calendar for nearby arts organizations, and there are some regional arts calendars.  But there is no 
	        single-source national or international arts calendar.  
	        
	        <p>
			The CalendArté website attempts to provide such a single-source comprehensive arts calendar.  
			An overarching goal of the website is to assist in audience development for participating organizations 
			and community cultural engagement.  The online calendaring system includes event, organization, venue, 
			artist and performer listings, related search functionality, and administrative facilities that allow 
			for the management of listings.  

			<p>
			There are two main audiences for the website: consumers and arts organization administrators.  
			Consumer users are able to search for events by date, artist, performer and/or venue. In the future, they 
			will be able to select a geographic location for searches, including their current location. 
			Relevant search result listings are displayed, 
			along with website links to each entity and links to online ticketing, when available.  
			Administrative users are able to log in and manage their events, applicable organization or venue 
			information and website links to their own website and ticketing services. 

			<p>
			the initial location for arts events is my hometown of Lancaster, PA.
			The long-term goal for CalendArté will be national in scope.
	        
